KV_OPTIONS = ["f16  — full precision (max VRAM)",
              "q8_0 — half VRAM, no quality loss (recommended)",
              "q4_0 — quarter VRAM, slight quality loss"]
KV_MAP     = ["f16", "q8_0", "q4_0"]


def screen_ip(cfg):
    clear(); header()
    section("Server IP Address")
    print()
    cfg.ip = prompt("Enter server IP", cfg.ip)
    success(f"IP set to {cfg.ip}")


def screen_services(cfg):
    clear(); header()
    section("Services")
    info("Toggle services on or off.")
    print()
    cfg.ollama_enabled     = prompt_bool("Enable Ollama (LLM server)?",      cfg.ollama_enabled)
    cfg.openwebui_enabled  = prompt_bool("Enable OpenWebUI (chat interface)?", cfg.openwebui_enabled)


def screen_gpu(cfg):
    clear(); header()
    section("GPU Configuration")
    info("This server has Tesla P40s (Pascal). The script always installs the")
    info("proprietary 550.x driver — the open kernel flavor does NOT support P40.")
    print()
    cfg.gpu_count = prompt_int("Number of P40 GPUs in this machine", 1, 8, cfg.gpu_count)
    total_vram = cfg.gpu_count * 24
    info(f"Total VRAM: {cfg.gpu_count} × 24 GB = {total_vram} GB")


def screen_paths(cfg):
    clear(); header()
    section("Storage Paths")
    info("All data stored under /mnt/disk0/Deepthought/ by default.")
    info("Models can be very large (70B q4 ≈ 42 GB, 70B q8 ≈ 80 GB).")
    print()
    cfg.models_dir = prompt(
        "Ollama models directory (where 'ollama pull' stores models)",
        cfg.models_dir
    )
    cfg.openwebui_data_dir = prompt(
        "OpenWebUI data directory",
        cfg.openwebui_data_dir
    )


def screen_ollama(cfg):
    if not cfg.ollama_enabled:
        return
    clear(); header()
    section("Ollama Options")
    print()
    cfg.ollama_keep_alive = prompt(
        "OLLAMA_KEEP_ALIVE (-1=indefinite, 0=unload after each request, 5m, 1h…)",
        cfg.ollama_keep_alive
    )
    cfg.ollama_max_loaded = prompt_int(
        "Max models resident in VRAM at once (1=safe, 2=multi-model)",
        1, 8, cfg.ollama_max_loaded
    )
    cfg.ollama_num_parallel = prompt_int(
        "Parallel inference requests (1 recommended for P40)",
        1, 16, cfg.ollama_num_parallel
    )
    default_kv = KV_MAP.index(cfg.ollama_kv_cache) if cfg.ollama_kv_cache in KV_MAP else 1
    ki = pick_menu("KV cache quantisation (saves VRAM on long contexts):", KV_OPTIONS, default_kv)
    cfg.ollama_kv_cache = KV_MAP[ki]
    print()
    info("Optional: model to pin in VRAM on container startup.")
    info("Example: qwen3:4b-instruct  (fast, low VRAM, good for always-on assistant)")
    info("Leave blank to skip.")
    cfg.ollama_preload_model = prompt("Preload model name", cfg.ollama_preload_model or "")


def screen_openwebui(cfg):
    if not cfg.openwebui_enabled:
        return
    clear(); header()
    section("OpenWebUI Options")
    print()
    cfg.openwebui_port = prompt_int(
        "OpenWebUI host port",
        1024, 65535, cfg.openwebui_port
    )
    info(f"OpenWebUI will be accessible at: http://{cfg.ip}:{cfg.openwebui_port}")


def show_summary(cfg):
    clear(); header()
    section("Configuration Summary")
    total_vram = cfg.gpu_count * 24
    print(f"\n  \033[0;37mHost IP:\033[0m     {cfg.ip}")
    print(f"  \033[0;37mGPUs:\033[0m        {cfg.gpu_count}x Tesla P40  ({total_vram} GB total VRAM)")
    print(f"  \033[0;37mDriver:\033[0m      550.x proprietary (from Debian 13 non-free)")
    print()
    print("  \033[1;37mServices:\033[0m")
    if cfg.ollama_enabled:
        print(f"    \033[1;32m✓\033[0m  Ollama        port 11434")
        print(f"           models dir:   {cfg.models_dir}")
        print(f"           keep_alive:   {cfg.ollama_keep_alive}")
        print(f"           max_loaded:   {cfg.ollama_max_loaded}")
        print(f"           kv_cache:     {cfg.ollama_kv_cache}")
