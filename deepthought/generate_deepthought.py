def main_menu(cfg):
    while True:
        clear(); header()
        total_vram = cfg.gpu_count * 24
        print("  \033[1;37mMain Menu\033[0m\n")
        print(f"    \033[0;33m1\033[0m.  Server IP             \033[0;36m{cfg.ip}\033[0m")
        print(f"    \033[0;33m2\033[0m.  Services              "
              f"Ollama={'on' if cfg.ollama_enabled else 'off'}  "
              f"OpenWebUI={'on' if cfg.openwebui_enabled else 'off'}")
        print(f"    \033[0;33m3\033[0m.  GPU count             "
              f"{cfg.gpu_count}x P40  ({total_vram} GB VRAM)")
        print(f"    \033[0;33m4\033[0m.  Storage paths")
        print(f"    \033[0;33m5\033[0m.  Ollama options")
        print(f"    \033[0;33m6\033[0m.  OpenWebUI options")
        print()
        print(f"    \033[0;33m7\033[0m.  Review summary")
        print(f"    \033[0;33m8\033[0m.  \033[1;32mGenerate files\033[0m")
        print(f"    \033[0;33m9\033[0m.  Quit")
        print()

        choice = prompt_int("Select option (1–9)", 1, 9, 8)

        if   choice == 1: screen_ip(cfg)
        elif choice == 2: screen_services(cfg)
        elif choice == 3: screen_gpu(cfg)
        elif choice == 4: screen_paths(cfg)
        elif choice == 5: screen_ollama(cfg)
        elif choice == 6: screen_openwebui(cfg)
        elif choice == 7:
            show_summary(cfg)
            prompt("Press Enter to continue", "")
        elif choice == 8:
            show_summary(cfg)
            print()
            if prompt_bool("Generate files now?", True):
                generate_files(cfg)
                return
        elif choice == 9:
            print("\n  Goodbye!\n")
            sys.exit(0)


