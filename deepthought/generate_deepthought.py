#  Generated by generate_deepthought.py
#
#  !! P40 DRIVER NOTES !!
#  - Pascal GPUs require the PROPRIETARY driver (nvidia-kernel-dkms).
#  - The OPEN kernel flavor (nvidia-open-kernel-dkms) does NOT support Pascal.
#  - Debian 13 Trixie ships the 550.x series in non-free which works fine.
#  - NVIDIA's CUDA repo does not yet support Debian 13, so we use Debian's
#    own nvidia-cuda-toolkit package (CUDA 12.x, compatible with P40).
#  - Debian 13 kernel 6.12 requires linux-headers to be installed FIRST
#    or DKMS cannot build the kernel module.
