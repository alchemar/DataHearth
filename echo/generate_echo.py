WHISPER_MODELS = [
    "tiny", "tiny-int8", "base", "base-int8",
    "small", "small-int8", "medium", "medium-int8",
    "large-v2", "large-v3",
]
KV_OPTIONS = ["f16  — full precision (max VRAM)",
              "q8_0 — half VRAM, no quality loss (recommended)",
              "q4_0 — quarter VRAM, slight quality loss"]
KV_MAP = ["f16", "q8_0", "q4_0"]


def screen_ip(cfg):
    clear(); header(); section("Server IP Address"); print()
    cfg.ip = prompt("Enter server IP", cfg.ip)
    success(f"IP set to {cfg.ip}")


def screen_services(cfg):
    clear(); header(); section("Services"); print()
    cfg.ollama_enabled  = prompt_bool("Enable Ollama (LLM server)?",         cfg.ollama_enabled)
    cfg.whisper_enabled = prompt_bool("Enable faster-whisper (STT server)?", cfg.whisper_enabled)


def screen_gpu(cfg):
    clear(); header(); section("GPU Configuration")
    info("GTX 1080 Ti is Pascal (sm_61). The script installs the proprietary")
    info("550.x driver from Debian non-free — correct for Pascal.")
    info("nvidia-open-kernel-dkms is NOT supported on Pascal.")
    print()
    has_gpu = prompt_bool("Does this server have an NVIDIA GPU?", cfg.gpu_driver == "nvidia")
    if has_gpu:
        cfg.gpu_driver = "nvidia"
        cfg.gpu_count  = prompt_int("How many GPUs?", 1, 8, cfg.gpu_count)
    else:
        cfg.gpu_driver = "none"
        cfg.gpu_count  = 0
        cfg.whisper_use_cuda = False
        warn("CPU-only mode — inference will be significantly slower.")


def screen_ollama(cfg):
    if not cfg.ollama_enabled:
        return
    clear(); header(); section("Ollama Options")
    info("GTX 1080 Ti has 11 GB VRAM. Models up to ~8B q8 fit comfortably.")
    print()
    cfg.ollama_models_dir = prompt("Models directory on host", cfg.ollama_models_dir)
    cfg.ollama_keep_alive = prompt("OLLAMA_KEEP_ALIVE (-1=indefinite, 0=unload, 5m, 1h…)",
                                   cfg.ollama_keep_alive)
    cfg.ollama_max_loaded   = prompt_int("Max models in VRAM (1 recommended)", 1, 4,
                                         cfg.ollama_max_loaded)
    cfg.ollama_num_parallel = prompt_int("Parallel requests (1 recommended)", 1, 8,
                                          cfg.ollama_num_parallel)
    ki = pick_menu("KV cache quantisation:", KV_OPTIONS,
                   KV_MAP.index(cfg.ollama_kv_cache) if cfg.ollama_kv_cache in KV_MAP else 1)
    cfg.ollama_kv_cache = KV_MAP[ki]
    print()
    info("Optional: model name to pin in VRAM on startup. Leave blank to skip.")
    cfg.ollama_preload_model = prompt("Preload model name", cfg.ollama_preload_model or "")


def screen_whisper(cfg):
    if not cfg.whisper_enabled:
        return
    clear(); header(); section("Whisper STT Options")
    info("small-int8 (~1 GB VRAM) recommended — leaves plenty of room for Ollama.")
    print()
    mi = pick_menu("Whisper model:", WHISPER_MODELS,
                   WHISPER_MODELS.index(cfg.whisper_model)
                   if cfg.whisper_model in WHISPER_MODELS else 5)
    cfg.whisper_model = WHISPER_MODELS[mi]
    cfg.whisper_lang  = prompt("Language code (en, fr, de, auto…)", cfg.whisper_lang)
    if cfg.gpu_driver == "nvidia":
        cfg.whisper_use_cuda = prompt_bool("Run Whisper on GPU (CUDA)?", True)
        if cfg.whisper_use_cuda and cfg.gpu_count > 1:
            cfg.whisper_gpu_index = prompt_int(
                f"Which GPU index for Whisper? (0–{cfg.gpu_count-1})",
                0, cfg.gpu_count - 1, cfg.gpu_count - 1)
    else:
        cfg.whisper_use_cuda = False


def show_summary(cfg):
    clear(); header(); section("Configuration Summary")
    print(f"\n  \033[0;37mHost IP:\033[0m   {cfg.ip}")
    print(f"  \033[0;37mGPU:\033[0m       "
          + (f"NVIDIA {cfg.gpu_count}x GTX 1080 Ti  (550.x proprietary, Pascal)"
             if cfg.gpu_driver == "nvidia" else "CPU-only"))
    print()
    print("  \033[1;37mServices:\033[0m")
    if cfg.ollama_enabled:
        print(f"    \033[1;32m✓\033[0m  Ollama          port 11434")
        print(f"           models: {cfg.ollama_models_dir}")
        print(f"           kv: {cfg.ollama_kv_cache}  keep: {cfg.ollama_keep_alive}")
        if cfg.ollama_preload_model:
            print(f"           preload: {cfg.ollama_preload_model}")
    else:
        print(f"    \033[0;90m✗  Ollama          (disabled)\033[0m")
    if cfg.whisper_enabled:
        backend = f"GPU {cfg.whisper_gpu_index} (CUDA)" if cfg.whisper_use_cuda else "CPU"
        print(f"    \033[1;32m✓\033[0m  faster-whisper  port 10300")
        print(f"           model: {cfg.whisper_model}  lang: {cfg.whisper_lang}  backend: {backend}")
    else:
        print(f"    \033[0;90m✗  faster-whisper  (disabled)\033[0m")
    print()


def main_menu(cfg):
    while True:
        clear(); header()
        print("  \033[1;37mMain Menu\033[0m\n")
        print(f"    \033[0;33m1\033[0m.  Server IP             \033[0;36m{cfg.ip}\033[0m")
        print(f"    \033[0;33m2\033[0m.  Services              "
              f"Ollama={'on' if cfg.ollama_enabled else 'off'}  "
              f"Whisper={'on' if cfg.whisper_enabled else 'off'}")
        print(f"    \033[0;33m3\033[0m.  GPU configuration     "
              + (f"NVIDIA · {cfg.gpu_count}x 1080 Ti"
                 if cfg.gpu_driver == "nvidia" else "CPU-only"))
        print(f"    \033[0;33m4\033[0m.  Ollama options")
        print(f"    \033[0;33m5\033[0m.  Whisper options")
        print()
        print(f"    \033[0;33m6\033[0m.  Review summary")
        print(f"    \033[0;33m7\033[0m.  \033[1;32mGenerate files\033[0m")
        print(f"    \033[0;33m8\033[0m.  Quit")
        print()
        choice = prompt_int("Select option (1–8)", 1, 8, 7)
        if   choice == 1: screen_ip(cfg)
        elif choice == 2: screen_services(cfg)
        elif choice == 3: screen_gpu(cfg)
        elif choice == 4: screen_ollama(cfg)
        elif choice == 5: screen_whisper(cfg)
        elif choice == 6:
            show_summary(cfg)
            prompt("Press Enter to continue", "")
        elif choice == 7:
            show_summary(cfg)
            print()
            if prompt_bool("Generate files now?", True):
                generate_files(cfg)
                return
        elif choice == 8:
            print("\n  Goodbye!\n")
            sys.exit(0)


# ──────────────────────────────────────────────────────────────────────────────
# Entry point
# ──────────────────────────────────────────────────────────────────────────────

def main():
    cfg = Config()
    main_menu(cfg)

if __name__ == "__main__":
    main()
