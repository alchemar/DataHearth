def generate_files(cfg):
    orig_cwd = os.getcwd()
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    os.chdir(OUTPUT_DIR)
    out_abs = os.path.abspath(".")

    parts = [gen_header(cfg.ip)]
    if cfg.ollama_enabled:
        parts.append(gen_ollama(cfg))
    if cfg.whisper_enabled:
        parts.append(gen_whisper(cfg))

    with open("docker-compose.yml", "w") as f:
        f.write("".join(parts))
    success("Written: docker-compose.yml")

    with open("setup.sh", "w") as f:
        f.write(gen_setup_script(cfg))
    os.chmod("setup.sh", 0o755)
    success("Written: setup.sh")

    with open("DEPLOY.md", "w") as f:
        f.write(gen_deploy_md(cfg))
    success("Written: DEPLOY.md")

    os.chdir(orig_cwd)

    print()
    print("\033[1;32m" + "=" * 60)
    print("  Echo files generated successfully!")
    print("=" * 60 + "\033[0m")
    print()
    print(f"  Output: \033[1;37m{out_abs}/\033[0m")
    print()
    print("  Next steps:")
    print(f"    1. \033[0;36mscp -r {OUTPUT_DIR}/ {cfg.ip}:~/\033[0m")
    print(f"    2. \033[0;36mssh {cfg.ip} 'cd {OUTPUT_DIR} && sudo bash setup.sh'\033[0m")
    print(f"    3. Reboot if prompted, then:")
    print(f"    4. \033[0;36mssh {cfg.ip} 'cd {OUTPUT_DIR} && docker compose up -d'\033[0m")
    print()


